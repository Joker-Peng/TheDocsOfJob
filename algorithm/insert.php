function insertSort($arr){
	$count=count($arr);
	for($i=1;$i<$count;$i++){
		$tem=$arr[$i];
		$j=$i-1;
		while($j>=0){
			if($arr[$j]>$tem){	
				$arr[$j+1]=$arr[$j];
				$arr[$j]=$tem;
			}
			$j--;
		}
	}	
	return  $arr;
}

