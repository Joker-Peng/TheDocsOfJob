 * 假设数组的前面几位是排序好的,便利出来数组后面的每一个元素,将这个元素和前面排序好的每一个元元素进行比较,
 * 当其和所要的排序要求相反时,就将这个元素和前面的元素交换位置;
 * 我们这里从数组的第二位开始遍历,第一次拿出数组的第二个元素和第一个元素进行对比,并将它们排序好;
 * 之后要进行的就是遍历出来数组之后的元素,和他后一位进行对比
 *
 * 时间复杂度: O(n*n)
 *
 * 实现数组的升序
 */
